package io.ytong.stanfordalgorithm.c3w4;

public class Item {
	private int value;
	private int weight;
	
	public Item(int value, int weight) {
		this.setValue(value);
		this.setWeight(weight);
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
	
}
